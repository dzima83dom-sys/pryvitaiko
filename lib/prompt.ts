import {
  getAgeLabel,
  getGenderLabel,
  getResolvedOccasion,
  getResolvedProfession,
  getResolvedRecipient,
} from '@/lib/mappings';
import type { GreetingState } from '@/store/useGreetingStore';

export function buildGreetingPrompt(state: GreetingState): string {
  const recipient = getResolvedRecipient(state);
  const profession = getResolvedProfession(state);
  const occasion = getResolvedOccasion(state);
  const gender = getGenderLabel(state.gender);
  const age = getAgeLabel(state.age);
  const maybeName = state.name.trim();

  const isChild = state.age === 'child';
  const recipientLine = recipient || 'не вказано';
  const professionLine = profession || 'не вказано';
  const occasionLine = occasion || 'не вказано';
  const genderLine = gender || 'не вказано';
  const ageLine = age || 'не вказано';

  return [
    'Ти пишеш українською мовою тепле, природне, людяне привітання, ніби його написала жива людина для однієї конкретної людини.',
    'Створи ОДНЕ готове привітання для відправки в месенджер.',
    '',
    'Дані:',
    `- Кому адресоване привітання: ${recipientLine}`,
    `- Сфера діяльності людини: ${professionLine}`,
    `- Нагода: ${occasionLine}`,
    `- Стать: ${genderLine}`,
    `- Вікова категорія: ${ageLine}`,
    `- Емоція: ${state.emotionLabel}`,
    `- Імʼя: ${maybeName || 'не вказано'}`,
    '',
    'Головні правила:',
    '- адресат ОДИН, не кілька людей',
    '- не перетворюй одного адресата на пару, сімʼю чи групу людей',
    '- не використовуй множину: "ви", "вас", "вам", якщо не вказано кількох адресатів',
    '- якщо вказано конкретну родинну роль, звертайся тільки до неї: мама, тато, брат, сестра, син, донька, бабуся, дідусь, хрещений, хрещена тощо',
    '- не змішуй ролі між собою: якщо адресат мама, не згадуй тата; якщо адресат тато, не згадуй маму',
    '- стать не повинна змінювати або розширювати конкретну родинну роль',
    '',
    'Вимоги до тексту:',
    '- тільки українська мова',
    '- 2 абзаци середньої довжини',
    '- текст має звучати природно, тепло і по-людськи',
    '- без канцеляриту, без штучного пафосу, без занадто літературних конструкцій',
    '- можна використовувати класичні побажання: здоровʼя, щастя, радості, тепла, добробуту, натхнення, гармонії',
    '- класичні побажання мають звучати природно і доречно, не перевантажуй ними весь текст',
    '- побажання міцного здоровʼя дозволені і бажані, якщо вони доречні',
    `- ${isChild ? 'не додавай побажання мирного неба, якщо адресат дитина' : 'можна доречно додати побажання мирного неба в кінці або ближче до фіналу'}`,
    '- якщо імʼя не вказано, не вигадуй його і не починай текст з імені',
    '- якщо імʼя вказано, використовуй його на початку тільки якщо це звучить природно',
    '- можна додати 1–2 емодзі тільки якщо вони реально доречні',
    '- не використовуй лапки навколо всього тексту',
    '- не додавай заголовків, варіантів, пояснень чи службового тексту',
    '- одразу дай фінальний текст для відправки',
  ].join('\n');
}